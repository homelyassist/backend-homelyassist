const stateToDistrictMap = {};
const districtToBlockMap = {};
const blockToVillageMap = {};

dataList.forEach(data => {
    // Populate stateToDistrictMap
    if (!stateToDistrictMap[data.state]) {
        stateToDistrictMap[data.state] = [];
    }
    stateToDistrictMap[data.state].push(data.district);

    // Populate districtToBlockMap and blockToVillageMap
    data.block.forEach(block => {
        // Populate districtToBlockMap
        if (!districtToBlockMap[data.district]) {
            districtToBlockMap[data.district] = [];
        }
        districtToBlockMap[data.district].push(block.name);

        // Populate blockToVillageMap
        blockToVillageMap[block.name] = block.village ? block.village.map(village => village.name) : [];
    });
});

